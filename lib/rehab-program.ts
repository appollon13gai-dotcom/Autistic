export function getActivitiesByCategory(category: string): RehabActivity[] {
  if (category === 'all') return rehabActivities;
  return rehabActivities.filter(a => a.category === category);
}

export function generateDailyPlan(count: number = 5): RehabActivity[] {
  // Простая генерация "тренировки на сегодня" - перемешиваем и берем
  const shuffled = [...rehabActivities].sort(() => 0.5 - Math.random());
  return shuffled.slice(0, Math.min(count, shuffled.length));
}

